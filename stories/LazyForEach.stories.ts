// Template 函数
export const Template: StoryFn = (args: any) => {
	// 渲染 LazyForEach 示例
	const renderItem = (item: string, index: number): TemplateResult => {
		// 随机高度模拟瀑布流效果
		return Column({})(
			ListItem(
				html`
					<div class="waterfall-item">${item} - ${index} - ${items.length}</div>
				`
			)
		);
	};

	// 使用 LazyForEach 渲染项目
	const lazyRenderedItems = LazyForEach(items, renderItem, args.threshold, {
		animationClass: "fade-in",
		duration: args.duration,
		delay: args.delay,
	});

	return html`
		${Row({})(
			List({
				className: "waterfall-container",
			})(lazyRenderedItems)
		)}

		<style>
			.waterfall-container {
				display: flex;
				flex-wrap: wrap;
				justify-content: center;
				align-items: flex-start;
				width: 100%;
				height: 100%;
			}
			.waterfall-item {
				width: 100%;
				height: 200px;
				margin: 10px;
				padding: 10px;
				border: 1px solid #ccc;
				border-radius: 5px;
				box-shadow: 0 0 10px rgba(0, 0, 0, 0.1);
				background-color: #fff;
				text-align: center;
				font-size: 16px;
				font-weight: bold;
				color: #333;
			}
			/* 动画效果 */
			@keyframes fadeIn {
				0% {
					opacity: 0;
					transform: translateY(20px);
				}
				100% {
					opacity: 1;
					transform: translateY(0);
				}
			}

			.fade-in {
				animation: fadeIn 0.2s ease-in-out forwards;
			}
		</style>
	`;
};

